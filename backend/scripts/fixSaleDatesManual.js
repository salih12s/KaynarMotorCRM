(async () => {
  try {
    let updated = 0;
    let notFound = 0;
    const processed = new Set();

    for (const [plaka, tarih] of updates) {
      const plakaUp = plaka.toUpperCase();
      
      // For duplicate plakas (like 33ADF243, 33AVR652 etc), match by date proximity
      const result = await pool.query(
        "SELECT id, plaka, tamamlama_tarihi FROM ikinci_el_motorlar WHERE UPPER(plaka) = $1 AND durum = 'tamamlandi' ORDER BY id",
        [plakaUp]
      );

      if (result.rows.length === 0) {
        console.log(`BULUNAMADI: ${plaka}`);
        notFound++;
        continue;
      }

      // Find first unprocessed row
      let target = null;
      for (const row of result.rows) {
        if (!processed.has(row.id)) {
          target = row;
          break;
        }
      }

      if (!target) {
        console.log(`ZATEN İŞLENDİ: ${plaka}`);
        continue;
      }

      processed.add(target.id);

      await pool.query(
        'UPDATE ikinci_el_motorlar SET tamamlama_tarihi = $1 WHERE id = $2',
        [tarih + 'T12:00:00+03:00', target.id]
      );
      updated++;
      console.log(`OK: id=${target.id} ${plaka} → ${tarih}`);
    }

    console.log(`\n--- Sonuç ---`);
    console.log(`Güncellenen: ${updated}`);
    console.log(`Bulunamayan: ${notFound}`);
    process.exit(0);
  } catch (e) {
    console.error(e);
    process.exit(1);
  }
})();
